#Arithmetic Operators
x = 20
y = 6

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)  #Modulus - it gives remainder after division

#Comparison Operators - Comparing values
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #Equal to
print(x != y) #Not equal to



#Assignment operators - used to assign/store value in value
number = 45
print(number)

number -= 2
print(number)

#logical operators - and, or, not
num1 = 67
num2 = 43

print(num1 == num2 and num1 > num2)
print(num1 == num2 or num1> num2)
print(not (num1 == num2 or num1> num2))